    static int findMergeNode(SinglyLinkedListNode head1, SinglyLinkedListNode head2) {
        var first = head1;
        var second = head2;

        while(first != second){
            
            if(first.next == null) first = head2;
            else first = first.next;
            if(second.next == null) second = head1;
            else second = second.next;
        }

        return second.data;
    }

